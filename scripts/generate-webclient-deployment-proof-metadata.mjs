import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outputPath = join(__dirname, '..', 'src', 'generated', 'webClientDeploymentProofMetadata.ts');
const required = process.env.HUSH_WEBCLIENT_DEPLOYMENT_PROOF_REQUIRED === 'true';

const read = (name, fallback = '') => {
  const value = process.env[name]?.trim();
  return value && value.length > 0 ? value : fallback;
};

const missing = [
  'HUSH_WEBCLIENT_DEPLOYMENT_PROOF_ID',
  'HUSH_WEBCLIENT_SOURCE_REF',
  'HUSH_WEBCLIENT_WEB_ARTIFACT_HASH',
  'HUSH_WEBCLIENT_CLIENT_BUNDLE_HASH',
  'HUSH_WEBCLIENT_DEPLOYMENT_TARGET',
].filter((name) => !process.env[name]?.trim());

if (required && missing.length > 0) {
  throw new Error(`Missing required WebClient deployment proof inputs: ${missing.join(', ')}`);
}

const metadata = {
  schemaVersion: 'hush-webclient-deployment-proof-handshake-v1',
  componentId: 'hush-web-client',
  deploymentProofId: read(
    'HUSH_WEBCLIENT_DEPLOYMENT_PROOF_ID',
    'local-dev-webclient-proof-not-claiming'
  ),
  deploymentTarget: read('HUSH_WEBCLIENT_DEPLOYMENT_TARGET', 'local-dev'),
  sourceRef: read('HUSH_WEBCLIENT_SOURCE_REF', 'local-dev-worktree'),
  webArtifactHash: read(
    'HUSH_WEBCLIENT_WEB_ARTIFACT_HASH',
    `sha256:${'0'.repeat(64)}`
  ),
  clientBundleHash: read(
    'HUSH_WEBCLIENT_CLIENT_BUNDLE_HASH',
    `sha256:${'0'.repeat(64)}`
  ),
  packageHash: read('HUSH_WEBCLIENT_PACKAGE_HASH', ''),
  publicPackageRef: read('HUSH_WEBCLIENT_PUBLIC_PACKAGE_REF', ''),
  deploymentProtocolVersion: 'hushvoting-deployment-protocol-v1',
  evidenceStatus: read('HUSH_WEBCLIENT_EVIDENCE_STATUS', 'accepted_with_limitations'),
  generatedAtUtc: read('HUSH_WEBCLIENT_GENERATED_AT_UTC', '1970-01-01T00:00:00.000Z'),
};

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  [
    '// This file is generated by scripts/generate-webclient-deployment-proof-metadata.mjs.',
    '// Local development uses a deterministic non-claiming fixture unless deployment proof env vars are supplied.',
    "import type { WebClientDeploymentProofMetadata } from '@/lib/deploymentProof/webClientDeploymentProofContract';",
    '',
    'export const webClientDeploymentProofMetadata: WebClientDeploymentProofMetadata =',
    `${JSON.stringify(metadata, null, 2)} as WebClientDeploymentProofMetadata;`,
    '',
  ].join('\n')
);
